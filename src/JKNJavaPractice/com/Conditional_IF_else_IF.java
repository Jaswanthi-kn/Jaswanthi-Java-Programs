public class Conditional_IF_else_IF {

	public static void main(String[] args) {
		int age=25;
		if(age>21)
		{
			System.out.println("am ready to enjoy");
		}
		else
		{
			System.out.println("am not ready");
		}
		if(age<=21)
		{
			System.out.println("am ready to enjoy");
		}
		else
		{
			System.out.println("am not ready");
		}
	}

}
